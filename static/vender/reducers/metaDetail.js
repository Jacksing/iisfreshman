import {ADD_META_DETAL, MOD_META_DETAIL, DEL_META_DETAIL} from '../constants/MetaDetailActions'

export default function metaDetail(state, action) {
    switch (action.type) {
    case ADD_META_DETAL:
        return state
    case MOD_META_DETAIL:
        return state
    case DEL_META_DETAIL:
        return state
    }
}